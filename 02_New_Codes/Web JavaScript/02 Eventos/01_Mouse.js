function clicar(){
    let a = document.getElementById(`area`);
    a.innerText=`Clicou!`;
    a.style.background=`blue`;
}

function entrar(){
    let a = document.getElementById(`area`);
    a.innerText=`Entrou!`;
    a.style.background=`green`;
}

function sair(){
    let a = document.getElementById(`area`);
    a.innerText=`Saiu!`;
    a.style.background=`red`;
}

from collections import deque
print("Filha")
fila= deque()
print(fila)
fila.append(3)
print(fila)
fila.append(6)
print(fila)
fila.append(9)
print(fila)
print(fila.popleft())
print(fila)

print("Pila")
pilha= deque()
print(pilha)
pilha.appendleft(3)
print(pilha)
pilha.appendleft(6)
print(pilha)
pilha.appendleft(9)
print(pilha)
print(pilha.popleft())




